package Operator_2;

public class Operator_4 {

	public static void main(String[] args) {
		String a = "durga";
		int b, c, d;
		b = 10;
		c = 20;
		d = 30;

		// The integers b, c, and d will be automatically converted to strings and then concatenated with the string "durga".
		a = a + b + c + d;
		System.out.println(a);
	}

}
